package loops;

public class ForLoop {
    public static void main(String[] args) {


        for (int i = 0; i < 10; i++) {
            System.out.println("this is a test" + (i + 1));

        }
        System.out.println("-------------------------");
        for (int i = 120; i > 100; i--) {
            System.out.println("this is a test" + i);

        }


    }
}